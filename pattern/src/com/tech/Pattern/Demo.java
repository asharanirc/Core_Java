package com.tech.Pattern;

public class Demo 
{
	public static void main(String[] args) 
	{
		    for ( int i=5; i>=1; i-- )
		    {
			     for (int j=1; j<=i; j++ )
			     {
				  System.out.print(" * ");
			     }
			     System.out.println();
		    }
			 
			 for (int i1=2; i1<=5; i1++)
	          {
	             for ( int j=1; j<=i1; j++)
	             { 
	        	   System.out.print(" * ");
	              }
	              System.out.println();
	         }
	}
}
	        


	
