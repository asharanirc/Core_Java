package com.tech.wrapperClass;

public class Float1 {

	public static void main(String[] args) 
	{
	   Float f = new Float(10.5);       
	   System.out.println(f);                //10.5
	   Float f1 = new Float(2.2f);
	   System.out.println(f1);                //2.2
	   Float f2 = new Float(5.0);
	   System.out.println(f2);                //5.5
	   
	}

}
